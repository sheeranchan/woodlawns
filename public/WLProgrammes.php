                <h2 class="theme-colour">Programme One: The Make A Difference Afro-Caribbean Senior Citizens Group</h2>
                <hr>
                <p>The Make a Difference group arranges coffee mornings, keep fit sessions for seniors and social events and has become a valuable community resource.</p>
                <p>The group was founded by Gloria Bailey in July 2003.</p>
                <p>After joining the Streatham Darby and Joan Club in 2002 – following the death of her husband, Mel – Gloria soon realised that the club had very few members from the black community, despite being based in an ethnically mixed area.</p>
                <p>So she set about promoting the club to friends and associates in the black community; many of whom, like Gloria, were already active in the voluntary sector. Make A Difference was born out of this grouping.</p>
                <p class="element-animate" data-animate-effect="fadeIn"><img src="img/subpage/programme_00.jpg" alt="Image placeholder" class="img-fluid"></p>
                <p>As the name suggests, the aim of the group is to make a difference in the local community by:</p>
                <ul>
                    <li>befriending the elderly</li>
                    <li>mentoring young people</li>
                    <li>encouraging others to get involved in volunteering</li>
                </ul>
                <h2>Regular meetings</h2>
                <hr>
                <p>The Make A Difference group meets on a regular basis at Woodlawns and has forged an affiliation with the Darby and Joan Club; the original, long-standing inhabitants of the venue. Woodlawns is now permanent home to both these organisations and to the Lambeth Asian Centre, which also shares the premises.</p>
                <p>The group has arranged coffee mornings, keep fit sessions for seniors and held a number of one-off social events, which have all helped to introduce Make A Difference to local people and other groups in the area. Ultimately, the aim of the group is to build a local network of volunteers; working to make a difference – in whatever way they can – in the community.</p>
                <p>Make A Difference organises annual events to celebrate Commonwealth Day, Black History Month and Remembrance Day. These events are open to all cultures and are now established as regular dates in the club calendar. Input from Make A Difference has contributed to raising the profile of Woodlawns; attracting lottery funding for the venue and helping to secure the existence of a valuable community resource.</p>
                <p>Make A Difference has established strong links with a variety of external agencies and organisations that also seek to serve the community; one such organisation being the Victoria League for Commonwealth Friendship, a branch of which has now been launched at Woodlawns.</p>

                <h2 class="theme-colour margin-top-5">Programme Two: Lambeth Asian Centre</h2>
                <hr>
                <p>LAC work tirelessly to ensure the highest standard of service provision for older people and carers in the Streatham area</p>
                <p class="element-animate" data-animate-effect="fadeIn"><img src="img/subpage/programme_01.jpg" alt="Image placeholder" class="img-fluid"></p>
                <p>Lambeth Asian Centre is a day centre for older Asian people in Streatham offering help, support and activities to promote their independence, health and well-being. It reaches out for the lonely and vulnerable people who live in this area.</p>
                <p>Lambeth Asian Centre was started on 28th of September 1998, initially the centre didn’t receive any funding. But the Chair Raj Acharya and the previous management committee decided to contribute £10.00 pounds every week. And with this help they were able to run the centre.</p>
                <p>In the year 2002 Streatham Darby and Joan Club generously welcomed us to their building. During 2004 Lambeth Asian Centre bid for Council funding and LAC and Streatham Darby and Joan Club decided to work in partnership. And later Make a Difference Black Afro Caribbean group also joined us. At present all the groups are running with the help of Council funding. Every year we have an A.G. M and elect new officers.</p>
                <p>The centre offers a number of activities for our members and users including:</p>
                <ul>
                    <li>Luncheon Club</li>
                    <li>Welfare and Housing Benefit</li>
                    <li>Religious Events</li>
                    <li>Sewing Classes</li>
                    <li>English Classes</li>
                    <li>Computer Classes</li>
                    <li>Day trips and Holidays</li>
                </ul>
