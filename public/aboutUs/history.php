                  <h4><strong>A short history of the Woodlawns Centre (The Streatham Darby & Joan Club) from construction in 1868 to the present day.</strong></h4>
                  <p class="element-animate" data-animate-effect="fadeIn"><img src="../img/subpage/woonlawns_history.jpg" alt="Image placeholder" class="img-fluid"></p>
                  <p>The area of modern Streatham lies on either side of Streatham High Road and Streatham Hill (the A23), an old roman road, running between, Christchurch Road (the south circular) and Norbury with the ancient parish church of St Leornards in the centre.</p>
                  <p>No16 the Woodlawns Centre (the Darby and Joan Club) was built as Barham House in 1868. It was built for the Drew family by the firm of George Trollope and sons and immediately became a family home for Joseph Harvey Trollope, one of George’s sons. It is known that this family firm constructed at least 17 houses along the road. In 1881 the Trollope household consisted of Joseph, his wife, son, daughter, cook, general maid, house maid, parlour maid, nurse maid and coachman. Over the years the property was leased or owned by the families of a number of wealthy businessmen. Henry Goedecker, a naturalised German moved in with his family in 1887. In 1891 Henry, a general merchant was living there with his wife, son and 6 servants. George Lewis Turner took over the property in 1896. At the age of 53 in 1901 he was living “on his own means” with his 24 year old wife and 3 young sons (all under 4 years) together with 6 servants. Alfred Longsdale Temple, a Lace Warehouseman and his family occupied the house between 1903 and 1910. By 1924 the house was occupied by its last family that of Sir Frederick Barthorpe J.P. who remained there until 1940.</p>
                  <p>Due to the efforts of David Robertson MP the house was acquired in 1943 for use by the Streatham Darby and Joan Club and has remained as an older persons centre ever since. Since that date the house has had many auspicious visitors. In June 1946 the club was visited by Queen Mary and in 1949 by George Wilkinson the chairman of the National Corporation for the care of old people. He had previously been Lord Mayor of London. The building now houses the Streatham Darby and Joan Club, the Lambeth Asian Centre and the Make A Difference Black and Afro-Caribbean Group. Many other groups are either based in or regularly use the building including the Streatham Society.</p>
